    private void populateCombo() {

        cmboIdNo.removeAll();

        for (int i = 0; i < arrLstHourlyEmp.size(); i++) {

            cmboIdNo.addItem(EmployeeDataEntry.arrLstHourlyEmp.get(i).getIdNumber() + "");

        }
        for (int i = 0; i < EmployeeDataEntry.arrLstSalariedEmp.size(); i++) {

            cmboIdNo.addItem(EmployeeDataEntry.arrLstSalariedEmp.get(i).getIdNumber() + "");

        }
        for (int i = 0; i < EmployeeDataEntry.arrLstManager.size(); i++) {

            cmboIdNo.addItem(EmployeeDataEntry.arrLstManager.get(i).getIdNumber() + "");

        }
    }

