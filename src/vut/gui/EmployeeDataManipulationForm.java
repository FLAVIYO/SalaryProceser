    private void cmboIdNoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmboIdNoActionPerformed

        final int MAX_HOURS = 40;
        String idNo = cmboIdNo.getSelectedItem().toString();

        for (int i = 0; i < EmployeeDataEntry.arrLstHourlyEmp.size(); i++) {
            if (EmployeeDataEntry.arrLstHourlyEmp.get(i).getIdNumber().equals(idNo)) {

                HourlyEmployeeClass hourlyEmpObj = EmployeeDataEntry.arrLstHourlyEmp.get(i);
                int hoursWorked = Integer.parseInt(JOptionPane.showInputDialog("Enter hours worked by the employee"));
                taDisplayData.setText(hourlyEmpObj.toString() + "\nPay:\t" + currFormat.format(hourlyEmpObj.weeklyPay(hoursWorked)) + "\n");
            }
        }
        for (int i = 0; i < EmployeeDataEntry.arrLstSalariedEmp.size(); i++) {
            if (EmployeeDataEntry.arrLstSalariedEmp.get(i).getIdNumber().equals(idNo)) {
                SalariedEmployeeClass salariedEmpObj = EmployeeDataEntry.arrLstSalariedEmp.get(i);
                taDisplayData.setText(salariedEmpObj.toString() + "\nPay:\t" + currFormat.format(salariedEmpObj.weeklyPay(MAX_HOURS)) + "\n");
            }
        }
        for (int i = 0; i < EmployeeDataEntry.arrLstManager.size(); i++) {
            if (EmployeeDataEntry.arrLstManager.get(i).getIdNumber().equals(idNo)) {
                ManagerClass managerObj = EmployeeDataEntry.arrLstManager.get(i);

                taDisplayData.setText(managerObj.toString() + "\nPay:\t" + currFormat.format(managerObj.weeklyPay(MAX_HOURS)) + "\n");
            }

        }


    }//GEN-LAST:event_cmboIdNoActionPerformed

